dotData Traject::get_data(uint32_t index, Operation::OperationType type){
  dotData tmp = data[index];
  if(type == Operation::TURN_RIGHT45 || type == Operation::TURN_RIGHT90){
    tmp.y = tmp.y;
    tmp.x = -tmp.x;
    tmp.rad = -tmp.rad;
  }
  return tmp;
}
dotData Traject::reverse_get_data(uint32_t index, Operation::OperationType type){
  uint32_t target_index = (data_size >= index + 1) ? data_size - 1 - index : 0;
  dotData tmp;
  dotData lastDot = end(type);
  dotData targetDot = get_data(target_index, type);
  tmp.x = lastDot.x - targetDot.x;
  tmp.y = lastDot.y - targetDot.y;
  tmp.rad = targetDot.rad;
  /*
  tmp.x = end(type).x - get_data(target_index, type).x;
  tmp.y = end(type).y - get_data(target_index, type).y;
  tmp.rad = get_data(target_index, type).rad;
  */
  return tmp;
}
Traject TrajectList::getTraject(Operation::OperationType type, Direction dir){
  uint8_t idx = 0;
  if(dir == NORTH) idx = 0;
  if(dir == EAST) idx = 1;
  if(dir == SOUTH) idx = 2;
  if(dir == WEST) idx = 3;
  if(type == Operation::TURN_LEFT45 || type == Operation::TURN_RIGHT45 || type == Operation::TURN_45) return turn45List[idx];
  if(type == Operation::TURN_LEFT135 || type == Operation::TURN_RIGHT135 || type == Operation::TURN_135) return turn135List[idx];
  if(type == Operation::LEFT_V90 || type == Operation::RIGHT_V90 || type == Operation::V90) return v90List[idx];
  else return turn45List[idx];
}
void TrajectList::setTraject(Traject traject){
  std::vector<Traject>* list_pointer;
  if(traject.get_type() == Operation::TURN_45) list_pointer = &turn45List;
  if(traject.get_type() == Operation::TURN_135) list_pointer = &turn135List;
  if(traject.get_type() == Operation::V90) list_pointer = &v90List;
  Matrix2f matrix = eigenRotate().cast<float>();
  for(size_t n = 0; n <= 3; n++){
    for(size_t i = 0; i < traject.real_size(); i++){
      dotData dot = traject.get_data(i);
      RowVector2f dotVec; dotVec << dot.x, dot.y;
      float target_x = (dotVec * matrix)(0);
      float target_y = (dotVec * matrix)(1);
      float target_rad = dot.rad;
      dotData tmp = {
        .x = target_x,
        .y = target_y,
        .rad = target_rad,
      };
      list_pointer->at(n).static_push_back(tmp);
    }
    matrix = matrix * left90Rotate().cast<float>(); //rotate
  }
}
